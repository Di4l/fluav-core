//-----------------------------------------------------------------------------
#include "devices.hpp"
//-----------------------------------------------------------------------------
using namespace nsUav;
//-----------------------------------------------------------------------------
TContainer<TDevice> Devices;
//-----------------------------------------------------------------------------

TDevice::TDevice(uav_id uiId) : TObject(uiId)
{
	m_uiKind  = UI_INVALID_ID;
	m_sMin    = 0;
	m_sMax    = 0;
	m_usLast  = 0;
	m_usValue = 0;
}
//-----------------------------------------------------------------------------

bool TDevice::modified()
{
	bool b_ret = m_usLast != m_usValue;
	m_usLast = m_usValue;
	return b_ret;
}
